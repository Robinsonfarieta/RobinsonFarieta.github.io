   public boolean ChecarNumeros(){
       if(miimcs.getCheckEmpty(txtpeso.getText(), txtaltura.getText())){
           JOptionpane.showMessageDialog(this,"campos vacio", "ERROR", 0);
           return false;
        }else {
           if(miimcs.getCheckIsNumeric()){
           return true;    
        }else{
            JOptionpane.showMessageDialog(this,"solo numeros", "ERROR", 0);
           return false;   
               
           }
       }
   }
    
    
    
